fun main() {
    print("Введите натуральное число: ")
    val n = readLine()?.toIntOrNull() ?: 0

    println("Число $n:")

    // а) проверка на четность
    if (n % 2 == 0) {
        println("  - является четным")
    } else {
        println("  - является нечетным")
    }

    // б) проверка окончания на 7
    if (n % 10 == 7) {
        println("  - оканчивается цифрой 7")
    } else {
        println("  - не оканчивается цифрой 7")
    }
}