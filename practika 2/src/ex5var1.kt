fun main() {
    print("Введите порядковый номер дня недели (1-7): ")
    val day = readLine()?.toIntOrNull() ?: 0

    val dayName = when (day) {
        1 -> "понедельник"
        2 -> "вторник"
        3 -> "среда"
        4 -> "четверг"
        5 -> "пятница"
        6 -> "суббота"
        7 -> "воскресенье"
        else -> "некорректный номер дня"
    }

    println("День недели: $dayName")
}